package ss1_introduction_in_java.thuc_hanh.hello_world;

public class Main {
    public static void main(String[] args) {
        int i = 10;
        double d = 3.0;
        float f = 5.0f;
        char c = 'a';
        String s = "Hà Nội";
        System.out.println("i = " + i);
        System.out.println("d = " + d);
        System.out.println("f = " + f);
        System.out.println("c = " + c);
        System.out.println("s = " + s);
    }
}
